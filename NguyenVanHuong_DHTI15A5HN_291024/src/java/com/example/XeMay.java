public class XeMay {
    private int ma;
    private String ten;
    private String hangSX;
    private String mau;

    public XeMay(int ma, String ten, String hangSX, String mau) {
        this.ma = ma;
        this.ten = ten;
        this.hangSX = hangSX;
        this.mau = mau;
    }

    public int getMa() {
        return ma;
    }

    public void setMa(int ma) {
        this.ma = ma;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public String getHangSX() {
        return hangSX;
    }

    public void setHangSX(String hangSX) {
        this.hangSX = hangSX;
    }

    public String getMau() {
        return mau;
    }

    public void setMau(String mau) {
        this.mau = mau;
    }

    
}
